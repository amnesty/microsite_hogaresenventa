        Las autoridades comunicaron a los y las inquilinas que sus condiciones en el alquiler no se verían afectadas por la venta. Sin embargo, los gastos de alquiler mensual en la práctica han subido porque los nuevos arrendadores añadían cargos adicionales, por servicios de seguridad o limpieza, o bien porque a los inquilinos se les denegó la prórroga de las ayudas a la vivienda que antes sí recibían.<br/><br/>

        Los fondos de inversión han desalojado a las personas que no podían seguir pagando sus alquileres supuestamente sociales. Todo ello ha ocurrido en un contexto de desinformación, miedo y estigmatización.<br/><br/>

        Las autoridades públicas no han llevado a cabo una evaluación del impacto (que incluyera una perspectiva de género) ni explicaron cómo y por qué consideraban justificada la venta de viviendas sociales cuando existía una demanda creciente de estas viviendas a consecuencia del aumento de los desalojos. En su actuación, las autoridades parecen no haber tenido en cuenta el hecho de que la vivienda social es un instrumento esencial para garantizar el derecho a la vivienda.<br/><br/>

        Por todo ello les pido que, en función de sus competencias:<br/><br/>

        • Ofrezcan igualdad de protección a los y las inquilinas de viviendas sociales de propiedad pública y a las de viviendas vendidas a las sociedades de inversión.<br/>
        • El gobierno autonómico debe restablecer las prestaciones sociales para garantizar que los y las inquilinas pueden hacer frente a todos los pagos.<br/>
        • Las autoridades locales y autonómicas deben evitar los desahucios de viviendas sociales sin la provisión de una vivienda alternativa.



